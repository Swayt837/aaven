const I18nContext = createContext(null)

export function I18nProvider({ children }) {
  const [lang, setLang] = useState(() => localStorage.getItem('bb_lang') || 'fr')

  const change = useCallback((l) => {
    setLang(l)
    localStorage.setItem('bb_lang', l)
    document.documentElement.lang = l
  }, [])

  const t = useCallback(
    (key, vars) => {
      let s = (dict[lang] && dict[lang][key]) || (dict.fr[key] ?? key)
      if (vars) for (const k in vars) s = s.replace(`{${k}}`, vars[k])
      return s
    },
    [lang]
  )

  return (
    <I18nContext.Provider value={{ lang, setLang: change, t }}>{children}</I18nContext.Provider>
  )
}

export function useI18n() {
  const ctx = useContext(I18nContext)
  if (!ctx) throw new Error('useI18n must be used within I18nProvider')
  return ctx
}
